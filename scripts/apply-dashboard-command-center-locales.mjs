const locales = ["az", "ru", "tr"];
const patched = [];

for (const locale of locales) {
  const translations = COMMAND_CENTER_TRANSLATIONS[locale];
  const missing = enKeys.filter((key) => !(key in translations));
  const extra = Object.keys(translations).filter((key) => !enKeys.includes(key));
  if (missing.length > 0) {
    throw new Error(`${locale}: missing commandCenter keys: ${missing.join(", ")}`);
  }
  if (extra.length > 0) {
    throw new Error(`${locale}: unexpected commandCenter keys: ${extra.join(", ")}`);
  }

  const filePath = path.join(messagesDir, `dashboard-workspace-${locale}.json`);
  const data = JSON.parse(fs.readFileSync(filePath, "utf8"));
  data.commandCenter = structuredClone(translations);
  fs.writeFileSync(filePath, `${JSON.stringify(data, null, 2)}\n`, "utf8");
  patched.push(`dashboard-workspace-${locale}.json`);
}

console.log(`Patched commandCenter in: ${patched.join(", ")}`);
console.log(`Applied ${enKeys.length} keys per locale.`);

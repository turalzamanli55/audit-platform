const en = JSON.parse(fs.readFileSync(path.join(messagesDir, "en.json"), "utf8"));
const enCc = en.planning.workspace.commandCenter;

for (const locale of ["az", "ru", "tr"]) {
  const filePath = path.join(messagesDir, `${locale}.json`);
  const data = JSON.parse(fs.readFileSync(filePath, "utf8"));
  const cc = data.planning?.workspace?.commandCenter;
  if (!cc) {
    console.warn(`No planning.workspace.commandCenter in ${locale}`);
    continue;
  }

  for (const key of Object.keys(enCc)) {
    if (patches[locale][key]) {
      cc[key] = patches[locale][key];
    } else if (typeof cc[key] === "string" && cc[key].includes("[[")) {
      cc[key] = cc[key].replace(/\[\[([^\]]+)\]\]/g, (_, token) => {
        const map = {
          PLANLAŞMA: "Planlaşdırma",
          PLANLAMA: "Planlama",
          TƏSDİQ: "Təsdiq",
          TƏQDİL: "Təsdiq",
          TƏSQİQ: "Təsdiq",
          MATERİALİYET: "Mahiyyətllik",
          MAHSULLUQ: "Mahiyyətllik",
          УТВЕРЖДЕНИЕ: "Утверждение",
          МАТЕРИАЛЬНОСТЬ: "Существенность",
          ONAY: "Onay",
          ÖNEMLİLİK: "Önemlilik",
          İNCELEME: "İnceleme",
          ОБЗОР: "Проверка",
        };
        return map[token] ?? token;
      });
    }
  }

  Object.assign(cc, patches[locale]);
  fs.writeFileSync(filePath, `${JSON.stringify(data, null, 2)}\n`, "utf8");
  console.log(`Patched planning.commandCenter in ${locale}.json`);
}

function selfHostedRunner(): string {
  return `#!/usr/bin/env bash
# Generated by EDRP — Self-hosted runner entrypoint
# Requires: Node 20+, npm. Optional: Docker + Supabase CLI for live reset.
set -euo pipefail

ROOT="$(cd "$(dirname "\${BASH_SOURCE[0]}")/../.." && pwd)"
cd "$ROOT"

export SKIP_RESET="\${SKIP_RESET:-1}"

echo "[EDRP] Installing dependencies"
npm ci

echo "[EDRP] Database governance"
npm run validate:database

if [[ "$SKIP_RESET" == "1" ]]; then
  echo "[EDRP] Operational release (skip live reset)"
  npm run edrp:operate -- --skip-reset
else
  echo "[EDRP] Operational release (full lifecycle)"
  npm run edrp:operate
fi

echo "[EDRP] Lint / test / build"
npm run lint
npm run test
npm run build

echo "[EDRP] Complete — see src/lib/devops/data/snapshots/latest.json"
`;
}

/**
 * Write production-ready CI/CD blueprints generated from DevOps Engine contracts.
 */
export function generateCiCdBlueprints(cwd = process.cwd()): {
  provider: CiProvider;
  path: string;
  blueprint: ReturnType<typeof buildCiBlueprint>;
}[] {
  const outputs: {
    provider: CiProvider;
    path: string;
    blueprint: ReturnType<typeof buildCiBlueprint>;
  }[] = [];

  const githubPath = join(cwd, ".github", "workflows", "edrp.yml");
  writeFile(githubPath, githubWorkflow());
  outputs.push({
    provider: "github_actions",
    path: githubPath,
    blueprint: buildCiBlueprint("github_actions"),
  });

  const azurePath = join(cwd, "azure-pipelines.yml");
  writeFile(azurePath, azurePipelines());
  outputs.push({
    provider: "azure_devops",
    path: azurePath,
    blueprint: buildCiBlueprint("azure_devops"),
  });

  const gitlabPath = join(cwd, ".gitlab-ci.yml");
  writeFile(gitlabPath, gitlabCi());
  outputs.push({
    provider: "gitlab_ci",
    path: gitlabPath,
    blueprint: buildCiBlueprint("gitlab_ci"),
  });

  const selfHostedPath = join(cwd, "ci", "self-hosted", "edrp.sh");
  writeFile(selfHostedPath, selfHostedRunner());
  outputs.push({
    provider: "self_hosted",
    path: selfHostedPath,
    blueprint: buildCiBlueprint("self_hosted"),
  });

  return outputs;
}

export function ensureCiCdBlueprints(cwd = process.cwd()): string[] {
  return generateCiCdBlueprints(cwd).map((entry) => entry.path);
}

export function ciCdBlueprintStatus(cwd = process.cwd()): {
  provider: CiProvider;
  present: boolean;
  path: string;
}[] {
  const expected: Array<{ provider: CiProvider; path: string }> = [
    { provider: "github_actions", path: join(cwd, ".github", "workflows", "edrp.yml") },
    { provider: "azure_devops", path: join(cwd, "azure-pipelines.yml") },
    { provider: "gitlab_ci", path: join(cwd, ".gitlab-ci.yml") },
    { provider: "self_hosted", path: join(cwd, "ci", "self-hosted", "edrp.sh") },
  ];
  return expected.map((entry) => ({
    ...entry,
    present: existsSync(entry.path),
  }));
}

import { existsSync, mkdirSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { buildCiBlueprint, PIPELINE_STAGES } from "@/lib/devops/ci";
import type { CiProvider } from "@/lib/devops/types";

function writeFile(path: string, contents: string): string {
  mkdirSync(dirname(path), { recursive: true });
  writeFileSync(path, contents, "utf8");
  return path;
}

function githubWorkflow(): string {
  return `# Generated by EDRP — Enterprise DevOps & Release Platform
# Do not duplicate governance logic here — invoke npm scripts that call devopsEngine.
name: EDRP

on:
  push:
    branches: [main, master]
  pull_request:
  workflow_dispatch:

concurrency:
  group: edrp-\${{ github.ref }}
  cancel-in-progress: true

jobs:
  validate:
    name: Validation Pipeline
    runs-on: ubuntu-latest
    timeout-minutes: 45
    steps:
      - uses: actions/checkout@v4
      - uses: actions/setup-node@v4
        with:
          node-version: "20"
          cache: npm
      - run: npm ci
      - name: Database governance
        run: npm run validate:database
      - name: EDRP operate (skip live reset in CI by default)
        run: npm run edrp:operate -- --skip-reset
      - name: Lint
        run: npm run lint
      - name: Unit tests
        run: npm run test
      - name: Build
        run: npm run build
      - name: Upload EDRP artifacts
        if: always()
        uses: actions/upload-artifact@v4
        with:
          name: edrp-artifacts
          path: |
            src/lib/devops/data/artifacts/**
            src/lib/devops/data/snapshots/latest.json
          if-no-files-found: warn

  # Stages mirrored for visibility (same engine — not duplicated logic)
  stage-matrix:
    name: Stage contract
    runs-on: ubuntu-latest
    needs: validate
    strategy:
      matrix:
        stage:
${PIPELINE_STAGES.map((stage) => `          - ${stage}`).join("\n")}
    steps:
      - run: echo "EDRP stage \${{ matrix.stage }} is enforced by devopsEngine pipeline"
`;
}

